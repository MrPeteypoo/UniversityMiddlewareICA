/// <summary>
/// Tags is a globally available container class which stores references to every tag used in the game.
/// Using Tags allows for the easy modification of tags without breaking the entire game.
/// </summary>
public sealed class Tags
{
	public const string enemy = "Enemy";
	public const string gameController = "GameController";
	public const string mainCamera = "MainCamera";
	public const string player = "Player";
	public const string playerTorch = "PlayerTorch";
	public const string stickySurface = "StickySurface";
	public const string tweenCamera = "TweenCamera";
	public const string textureCamera = "TextureCamera";
}
